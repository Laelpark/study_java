package com.laelcompany.study.ch02;

public class IntExample {
// class 파일이름이랑 동일해야 함. 첫글자는 무조건 대문자
	
	public static void main(String[] args) {
		
		int var1 = 10;      // 십진수
		int var2 = 012;     // 8진수
		int var3 = 0xa;     // 16진수
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
	}
}
